/************************************************************************/
/*                     VGA Register Programming                        */
/************************************************************************/

/*
 * Switch to VGA mode 0x13 (320×200×256).
 * This reprograms the CRT Controller, Sequencer, Graphics Controller,
 * and Attribute Controller with the register values for mode 0x13.
 */

/* CRT Controller registers for mode 0x13 */
static const uint8_t crtc_0x13[] = {
    0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0xBF, 0x1F,
    0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x9C, 0x0E, 0x8F, 0x28, 0x40, 0x96, 0xB9, 0xA3,
    0xFF
};

static void vga_write_regs(struct platform_bus_ops* ops,
                           uint16_t addr_port, uint16_t data_port,
                           const uint8_t* regs, size_t count)
{
    for (size_t i = 0; i < count; i++) {
        ops->out_port8(addr_port, (uint8_t)i);
        ops->out_port8(data_port, regs[i]);
    }
}

static void vga_set_mode_0x13(struct platform_bus_ops* ops)
{
    if (!ops) return;

    /* 1. Disable interrupts / save state by reading status */
    ops->in_port8(0x3DA);  /* reset attribute flip-flop */

    /* 2. Set Misc Output Register for 28 MHz dot clock, 320-pixel mode */
    ops->out_port8(VGA_MISC_WRITE, 0x63);

    /* 3. Disable sequencer during reprogramming */
    ops->out_port8(VGA_SEQ_ADDR, 0x00);
    ops->out_port8(VGA_SEQ_DATA, 0x01);  /* synchronous reset */

    /* 4. Program Sequencer registers */
    {
        static const uint8_t seq[] = {
            0x03, 0x01, 0x0F, 0x00, 0x0E
        };
        vga_write_regs(ops, VGA_SEQ_ADDR, VGA_SEQ_DATA, seq, 5);
    }

    /* 5. Re-enable sequencer */
    ops->out_port8(VGA_SEQ_ADDR, 0x00);
    ops->out_port8(VGA_SEQ_DATA, 0x03);

    /* 6. Unlock CRTC (unlock write to CRTC index 0-7) */
    ops->out_port8(VGA_CRT_ADDR, 0x11);
    ops->out_port8(VGA_CRT_DATA,
                   (uint8_t)(ops->in_port8(VGA_CRT_DATA) & 0x7F));

    /* 7. Program CRTC registers */
    vga_write_regs(ops, VGA_CRT_ADDR, VGA_CRT_DATA, crtc_0x13, 25);

    /* 8. Program Graphics Controller registers */
    {
        static const uint8_t gc[] = {
            0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x05, 0x0F, 0xFF
        };
        vga_write_regs(ops, VGA_GC_ADDR, VGA_GC_DATA, gc, 9);
    }

    /* 9. Program Attribute Controller registers */
    {
        static const uint8_t ac[] = {
            0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
            0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
            0x41
        };
        ops->in_port8(0x3DA);  /* reset flip-flop */
        for (size_t i = 0; i < 17; i++) {
            ops->out_port8(VGA_AC_ADDR, (uint8_t)i);
            ops->out_port8(VGA_AC_DATA, ac[i]);
        }
        /* Enable palette / text mode off */
        ops->out_port8(VGA_AC_ADDR, 0x20);
    }

    /* 10. Set DAC palette (default 256-color palette) */
    {
        static const uint8_t default_palette[256][3] = {
            /* 0-15: standard EGA/VGA colors */
            {0x00,0x00,0x00},{0x00,0x00,0x2A},{0x00,0x2A,0x00},{0x00,0x2A,0x2A},
            {0x2A,0x00,0x00},{0x2A,0x00,0x2A},{0x2A,0x15,0x00},{0x2A,0x2A,0x2A},
            {0x15,0x15,0x15},{0x15,0x15,0x3F},{0x15,0x3F,0x15},{0x15,0x3F,0x3F},
            {0x3F,0x15,0x15},{0x3F,0x15,0x3F},{0x3F,0x3F,0x15},{0x3F,0x3F,0x3F},
            /* 16-31: grayscale */
            {0x00,0x00,0x00},{0x05,0x05,0x05},{0x08,0x08,0x08},{0x0B,0x0B,0x0B},
            {0x0E,0x0E,0x0E},{0x11,0x11,0x11},{0x14,0x14,0x14},{0x18,0x18,0x18},
            {0x1C,0x1C,0x1C},{0x20,0x20,0x20},{0x24,0x24,0x24},{0x28,0x28,0x28},
            {0x2D,0x2D,0x2D},{0x32,0x32,0x32},{0x38,0x38,0x38},{0x3F,0x3F,0x3F},
            /* 32-247: color wheel (simplified: ramp) */
        };
        ops->out_port8(VGA_DAC_MASK, 0xFF);
        ops->out_port8(VGA_DAC_WRITE, 0);
        for (int i = 0; i < 16; i++) {
            ops->out_port8(VGA_DAC_DATA, default_palette[i][0]);
            ops->out_port8(VGA_DAC_DATA, default_palette[i][1]);
            ops->out_port8(VGA_DAC_DATA, default_palette[i][2]);
        }
        /* 16-31: grayscale */
        for (int i = 16; i < 32; i++) {
            ops->out_port8(VGA_DAC_DATA, default_palette[i][0]);
            ops->out_port8(VGA_DAC_DATA, default_palette[i][1]);
            ops->out_port8(VGA_DAC_DATA, default_palette[i][2]);
        }
        /* 32-255: color cube (6×6×6 = 216 colors) */
        for (int i = 32; i < 256; i++) {
            int idx = i - 32;
            uint8_t r = (uint8_t)((idx / 36) % 6 * 0x0C);
            uint8_t g = (uint8_t)((idx / 6)  % 6 * 0x0C);
            uint8_t b = (uint8_t)( idx        % 6 * 0x0C);
            ops->out_port8(VGA_DAC_DATA, r);
            ops->out_port8(VGA_DAC_DATA, g);
            ops->out_port8(VGA_DAC_DATA, b);
        }
    }
}

/************************************************************************/
/*                      Driver Probe / Remove                           */
/************************************************************************/

static int framebuffer_probe(struct device* dev)
{
    struct platform_device* pdev = to_platform_device(dev);
    gfx_dev.bus_ops = platform_device_get_ops(pdev);

    gfx_dev.lock = spinlock_alloc();
    if (!gfx_dev.lock)
        return -1;

    gfx_dev.fb = (uint8_t*)GFX_BUF_ADDR;

    /* Switch to graphics mode */
    vga_set_mode_0x13(gfx_dev.bus_ops);

    /* Clear the screen */
    gfx_clear(GFX_GREEN);

    return 0;
}

static int framebuffer_remove(struct device* dev)
{
    (void)dev;

    /* Return to text mode: set mode 0x03 (80×25 text) via BIOS-compatible
     * register programming.  A full VGA mode-3 register table would go here.
     * For now, the simplest approach is to let the next boot reset the card. */

    if (gfx_dev.lock) {
        spinlock_release(gfx_dev.lock);
        gfx_dev.lock = NULL;
    }

    return 0;
}

static struct driver framebuffer_driver = {
    .type   = "framebuffer",
    .probe  = framebuffer_probe,
    .remove = framebuffer_remove,
};

/************************************************************************/
/*                    Framebuffer Public API                            */
/************************************************************************/

void gfx_init(void)
{
    platform_driver_register(&framebuffer_driver);
}

void gfx_exit(void)
{
    platform_driver_unregister(&framebuffer_driver);
}

void gfx_clear(uint8_t color)
{
    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);

    uint8_t* fb = dev->fb;
    size_t total = GFX_WIDTH * GFX_HEIGHT;
    for (size_t i = 0; i < total; i++)
        fb[i] = color;

    dev->curr_col = 0;
    dev->curr_row = 0;

    spinlock_unlock(dev->lock);
}

void gfx_put_pixel(size_t x, size_t y, uint8_t color)
{
    if (x >= GFX_WIDTH || y >= GFX_HEIGHT)
        return;

    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);
    dev->fb[y * GFX_WIDTH + x] = color;
    spinlock_unlock(dev->lock);
}

void gfx_fill_rect(size_t x, size_t y, size_t w, size_t h, uint8_t color)
{
    if (x >= GFX_WIDTH || y >= GFX_HEIGHT) return;
    if (x + w > GFX_WIDTH)  w = GFX_WIDTH  - x;
    if (y + h > GFX_HEIGHT) h = GFX_HEIGHT - y;

    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);

    uint8_t* fb = dev->fb;
    for (size_t row = y; row < y + h; row++) {
        size_t offset = row * GFX_WIDTH + x;
        for (size_t col = 0; col < w; col++)
            fb[offset + col] = color;
    }

    spinlock_unlock(dev->lock);
}

void gfx_put_char(char c, size_t col, size_t row, uint8_t fg, uint8_t bg)
{
    if (col >= GFX_COLS || row >= GFX_ROWS)
        return;

    /* Only render printable ASCII */
    if (c < 32 || c > 126)
        c = ' ';

    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);

    const uint8_t* glyph = font_8x16[c - 32];
    uint8_t* fb = dev->fb;
    size_t px = col * FONT_WIDTH;
    size_t py = row * FONT_HEIGHT;

    for (size_t fy = 0; fy < FONT_HEIGHT; fy++) {
        uint8_t row_bits = glyph[fy];
        size_t offset = (py + fy) * GFX_WIDTH + px;
        for (size_t fx = 0; fx < FONT_WIDTH; fx++) {
            fb[offset + fx] = (row_bits & (0x80 >> fx)) ? fg : bg;
        }
    }

    spinlock_unlock(dev->lock);
}

void gfx_write(const char* str, size_t col, size_t row, uint8_t fg, uint8_t bg)
{
    if (!str) return;

    size_t x = col, y = row;
    while (*str) {
        if (*str == '\n') {
            x = col;
            y++;
        } else {
            gfx_put_char(*str, x, y, fg, bg);
            x++;
            if (x >= GFX_COLS) {
                x = col;
                y++;
            }
        }
        if (y >= GFX_ROWS) break;
        str++;
    }
}

void gfx_scroll(uint8_t bg)
{
    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);

    uint8_t* fb = dev->fb;
    size_t row_bytes = GFX_WIDTH * FONT_HEIGHT;

    /* Move all rows up by one character row */
    for (size_t y = 0; y < (GFX_ROWS - 1) * FONT_HEIGHT; y++) {
        size_t src = (y + FONT_HEIGHT) * GFX_WIDTH;
        size_t dst = y * GFX_WIDTH;
        for (size_t x = 0; x < GFX_WIDTH; x++)
            fb[dst + x] = fb[src + x];
    }

    /* Clear bottom row */
    size_t last_start = (GFX_ROWS - 1) * FONT_HEIGHT * GFX_WIDTH;
    for (size_t i = 0; i < row_bytes; i++)
        fb[last_start + i] = bg;

    spinlock_unlock(dev->lock);
}

void gfx_putchar(char c)
{
    struct framebuffer_device* dev = &gfx_dev;

    spinlock_lock(dev->lock);

    if (c == '\n') {
        dev->curr_col = 0;
        dev->curr_row++;
    } else if (c == '\b') {
        if (dev->curr_col > 0) {
            dev->curr_col--;
        } else if (dev->curr_row > 0) {
            dev->curr_row--;
            dev->curr_col = GFX_COLS - 1;
        }
        /* Erase the character cell */
        gfx_put_char(' ', dev->curr_col, dev->curr_row,
                     dev->curr_fg, dev->curr_bg);
    } else if (c >= ' ') {
        gfx_put_char(c, dev->curr_col, dev->curr_row,
                     dev->curr_fg, dev->curr_bg);
        dev->curr_col++;
        if (dev->curr_col >= GFX_COLS) {
            dev->curr_col = 0;
            dev->curr_row++;
        }
    }

    if (dev->curr_row >= GFX_ROWS) {
        dev->curr_row = GFX_ROWS - 1;
        spinlock_unlock(dev->lock);
        gfx_scroll(dev->curr_bg);
        spinlock_lock(dev->lock);
    }

    spinlock_unlock(dev->lock);
}

void gfx_get_cursor(size_t* col, size_t* row)
{
    if (col) *col = gfx_dev.curr_col;
    if (row) *row = gfx_dev.curr_row;
}

void gfx_set_cursor(size_t col, size_t row)
{
    if (col >= GFX_COLS) col = GFX_COLS - 1;
    if (row >= GFX_ROWS) row = GFX_ROWS - 1;
    gfx_dev.curr_col = col;
    gfx_dev.curr_row = row;
}

